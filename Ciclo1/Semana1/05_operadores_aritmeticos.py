# suma +
numeroUno = 11
numeroDos = 5
print(numeroUno + 5 + numeroDos)

# resta -
print(numeroDos-numeroUno)

# multiplicacion *
print(numeroUno*numeroDos)

# division /
print(numeroUno/numeroDos)

# divion solo con parte entera //
print(numeroUno//numeroDos)

# potencia **
print(numeroUno**numeroDos)

# modulo - residuo de una division %
print(numeroUno%numeroDos)