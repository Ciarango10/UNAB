//CICLO WHILE
let contador = 0;

while (contador < 10) {
    console.log(contador);
    contador++;
}

//CICLO DO-WHILE
let contador2 = 25;

do {
    console.log(contador2);
    contador2++;
} while (contador2 < 20);

//FOR
for (let contador3 = 30; contador3 <= 60; contador3++) {
    console.log(contador3);
}