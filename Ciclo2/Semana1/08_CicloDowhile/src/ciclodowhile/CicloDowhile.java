
package ciclodowhile;


public class CicloDowhile {

  
    public static void main(String[] args) {
       //**************************************
       //CICLO DO WHILE 
       //**************************************
       int contador = 0;
       
       do{
           System.out.print("contador = " + contador);
           contador = contador + 1;
       }while(contador < 10);
    }
    
}
